# Helper functions for unit data access
def get_units_for_species(species_name: str) -> list:
    """Get all unit instances for a specific species."""
    # Handle both display name and key formats
    return [
        unit
        for unit in UNIT_DATA
        if unit.species.name.upper() == species_name.upper()
        or unit.species.name == species_name
    ]


def get_unit_by_id(unit_type_id: str):
    """Get a specific unit instance by its type_id."""
    for unit in UNIT_DATA:
        if unit.unit_id == unit_type_id:
            return unit
    return None


def get_all_species() -> list:
    """Get a list of all species names."""
    species_names = set()
    for unit in UNIT_DATA:
        species_names.add(unit.species.name)
    return list(species_names)


def get_units_by_class(unit_class_type: str) -> list:
    """Get all units of a specific class type across all species."""
    return [unit for unit in UNIT_DATA if unit.unit_type == unit_class_type]


def validate_unit_data_integrity() -> bool:
    """Validate the integrity of all unit data. Returns True if all data is valid."""
    valid_unit_classes = [
        "Heavy Melee",
        "Light Melee",
        "Cavalry",
        "Missile",
        "Magic",
        "Monster",
    ]
    valid_health_values = [1, 2, 3, 4]

    unit_ids = set()

    for unit in UNIT_DATA:
        # Check that it's a UnitModel instance
        if not isinstance(unit, UnitModel):
            print(f"ERROR: Unit is not a UnitModel instance: {unit}")
            return False

        # Check for duplicate IDs
        unit_id = unit.unit_id
        if unit_id in unit_ids:
            print(f"ERROR: Duplicate unit type_id: {unit_id}")
            return False
        unit_ids.add(unit_id)

        # Validate field values
        if not isinstance(unit.name, str) or not unit.name.strip():
            print(f"ERROR: Invalid name in {unit_id}")
            return False

        if unit.max_health not in valid_health_values:
            print(f"ERROR: Invalid max_health {unit.max_health} in {unit_id}")
            return False

        if unit.unit_type not in valid_unit_classes:
            print(f"ERROR: Invalid unit_type '{unit.unit_type}' in {unit_id}")
            return False

        # Validate species reference
        if not hasattr(unit.species, "name"):
            print(f"ERROR: Invalid species reference in {unit_id}")
            return False

    print(f" All {len(unit_ids)} unit instances validated successfully")
    return True
